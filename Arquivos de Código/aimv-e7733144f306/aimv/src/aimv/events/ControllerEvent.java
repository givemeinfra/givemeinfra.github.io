package aimv.events;

import aimv.controllers.AIMV;


public class ControllerEvent {
	
	private AIMV controller;
	public String property;
	public Object previousValue;
	public Object afterValue;
	
	public ControllerEvent(AIMV controller) {
		this.controller = controller;
	}

	public AIMV getController() {
		return controller;
	}

	
}//class
