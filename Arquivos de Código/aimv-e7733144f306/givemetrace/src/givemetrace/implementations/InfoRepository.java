package givemetrace.implementations;

public class InfoRepository {
	
	String projectName;
	String repositoryPath;
	String repositoryType;
	String workCopyPath;
	String login;
	String password;
	
	
	public InfoRepository() {
		super();
		return;	
	}
	public String getProjectName() {
		return projectName;
	}
	public void setProjectName(String projectName) {
		this.projectName = projectName;
	}
	public String getRepositoryPath() {
		return repositoryPath;
	}
	public void setRepositoryPath(String repositoryPath) {
		this.repositoryPath = repositoryPath;
	}
	public String getRepositoryType() {
		return repositoryType;
	}
	public void setRepositoryType(String repositoryType) {
		this.repositoryType = repositoryType;
	}
	
	public String toString(){
		return "Project Name: "+projectName+"\nURL: "+repositoryPath+"\nType: "+repositoryType; 
		
	}
	public String getLogin() {
		return login;
	}
	public void setLogin(String login) {
		this.login = login;
	}
	public String getPassword() {
		return password;
	}
	public void setPassword(String password) {
		this.password = password;
	}
	public String getWorkCopyPath() {
		return workCopyPath;
	}
	public void setWorkCopyPath(String workCopyPath) {
		this.workCopyPath = workCopyPath;
	}
}
