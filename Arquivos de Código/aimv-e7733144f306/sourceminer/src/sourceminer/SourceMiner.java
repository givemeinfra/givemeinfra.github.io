package sourceminer;

import org.eclipse.ui.plugin.AbstractUIPlugin;

public class SourceMiner extends AbstractUIPlugin {

	private static SourceMiner defaultInstance;
	
	public static boolean logstart;
	
	public SourceMiner(){
		SourceMiner.setDefault(this);
	}
	
	private static void setDefault(SourceMiner instance){
		SourceMiner.defaultInstance = instance;
	}
	
	public static SourceMiner getDefault(){
		return SourceMiner.defaultInstance;
	}

}
